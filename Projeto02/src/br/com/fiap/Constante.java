package br.com.fiap;

public class Constante {
    public static void main(String[] args) {
        //declaração e inicialização de constantes
        final double PI = 3.1415;
        final float GRAVIDADE_TERRESTRE = 9.8f;

        // Sequeência de Escape: \n (pula de linha)
        // Sequência de Escape: \t (tabulação)
        System.out.println("Valor de PI:\t\t" + PI + "\nGravidade da Terra: " + GRAVIDADE_TERRESTRE);
    }
}
